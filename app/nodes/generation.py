def _should_prefer_ai(state: ModernizationState) -> bool:
    confidence = state.get("confidence", {})
    parsing = float(confidence.get("parsing", 1.0))
    semantic = float(confidence.get("semantic_analysis", 1.0))
    return parsing < 0.8 or semantic < 0.8 or bool(state.get("errors"))


def generate_python(state: ModernizationState) -> ModernizationState:
    report = state.setdefault("report", {"stages": {}, "decisions": [], "risks": []})
    context = build_generation_context(state)
    provider = get_ai_provider()
    prompt = build_generation_prompt(state)
    prefer_ai = _should_prefer_ai(state)
    ai_result = provider.generate(prompt)

    if ai_result.available and ai_result.text.strip():
        code = extract_python_code(ai_result.text)
        state["generated_code"] = code
        state["generation_strategy"] = "ai"
        state["ai_context"] = prompt_summary(prompt)
        state["ai_usage"] = ai_result.usage
        report["stages"]["generation"] = {
            "status": "ok",
            "strategy": "ai",
            "provider": ai_result.provider,
            "model": ai_result.model,
            "prompt": prompt_summary(prompt),
            "usage": ai_result.usage,
            "preferred_due_to_low_confidence": prefer_ai,
        }
        report["decisions"] = list(
            dict.fromkeys(
                [
                    *report.get("decisions", []),
                    "AI generation used structured parser and semantic-analysis context.",
                ]
            )
        )
        return state

    body, decisions, routine_name = _deterministic_generate(state)
    strategy = "deterministic_fallback" if prefer_ai or ai_result.error else "deterministic"
    state["generated_code"] = body
    state["generation_strategy"] = strategy
    report["stages"]["generation"] = {
        "status": "ok",
        "routine_name": routine_name,
        "strategy": strategy,
        "ai_provider": ai_result.provider,
        "ai_model": ai_result.model,
        "ai_unavailable_reason": ai_result.error,
        "preferred_ai_due_to_low_confidence": prefer_ai,
        "context_keys": sorted(context.keys()),
        "decisions": decisions,
        "prompt": prompt_summary(prompt),
    }
    report["decisions"] = list(dict.fromkeys([*report.get("decisions", []), *decisions]))
    return state
