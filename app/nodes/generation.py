def _routine_name(state: ModernizationState) -> str:
    findings = state.get("semantic_findings", {})
    routine = findings.get("routine", {}) if isinstance(findings, dict) else {}
    return str(routine.get("name") or "modernized_procedure")


def _parameter_names(state: ModernizationState) -> list[str]:
    findings = state.get("semantic_findings", {})
    params = findings.get("parameters", []) if isinstance(findings, dict) else []
    names = [str(param["name"]) for param in params if param.get("direction", "IN") != "OUT"]
    return names or ["connection"]


def _signature_params(state: ModernizationState) -> str:
    names = _parameter_names(state)
    if "connection" not in names:
        names = ["connection", *names]
    return "\n".join(f"    {name}: object," for name in names)


def _sql_constant(source: str) -> str:
    escaped = source.strip().replace('"""', r"\"\"\"")
    return f'"""\\\n{escaped}\n"""'


